const dict: Record<Lang, Record<string, string>> = { zh, en }

let _lang: Lang = 'zh'

export function setLang(l: Lang) { _lang = l }

export function getLang(): Lang { return _lang }

export function t(key: string, ...args: (string | number)[]): string {
  const found = dict[_lang]?.[key] || dict.en[key]
  // First string arg acts as a default when the key is missing entirely.
  let s = found || (typeof args[0] === 'string' ? args.shift() as string : key)
  args.forEach((a, i) => { s = s.replace(`{${i}}`, String(a)) })
  return s
}
